#할당(대입)연산자
#할당 연산자는 변수의 값을 대입하는연산자로 대입 연산자라고도 합니다.
num = 5
#할당연산자(=)
#복합대입연산자 (+=, -= ,*=,/=,%=, //=, **= )
num += 5
#설명: 현재 num 값에 5를 더한 후 다시 num에 저장합니다.
num -= 5
#설명: 현재 num 값에 5를 뺀 후 다시 num에 저장합니다.
num *= 5
#설명: 현재 num 값에 5를 곱한 후 다시 num에 저장합니다.
num /= 5
#설명: 현재 num 값에 5를 나눈 후 다시 num에 저장합니다.
num %= 5
#설명 num을 5로 나눴을때 나머지를 구해서 num에 저장합니다.
num //= 5
#설명 num을 5로 나눴을때 소수점을 버린몫만 num에 저장합니다.
num **= 5
#설명 num의 5제곱을 구해서 num에 저장합니다.


